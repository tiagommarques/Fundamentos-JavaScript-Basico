/*
    let = declaração de variável que pode ser reatribuída a qualquer momento; 
    var = declaração de variável (não recomendado) 
        que pode ser reatribuída a qualquer momento;
    const = declaração de variável que não pode ser reatribuída a qualquer momento;
    identificador = nome da variável; 
        exemplo: nome, idade, preco, imposto, precoFinal;
    valor = valor da variável;
        exemplo: "Lapis", 10, 6.4, 1.5, precoFinal;
    tipo = tipo da variável;
        exemplo: string, number, number, number, number;
    tipo de dado = tipo da variável;
        exemplo: string, number, number, number, number;
    tipo de variável = tipo da variável;
        exemplo: string, number, number, number, number;

    valor literal = valor da variável;
        exemplo: "Lapis", 10, 6.4, 1.5, precoFinal;

    string = sequência de caracteres, letras, números, espaços, simbolos, etc;
        exemplo: "Lapis", "10", "6.4", "1.5", "precoFinal";
    ponto flutuante = número com casas decimais;
        exemplo: 6.4, 1.5;
    number = número inteiro ou decimal;
        exemplo: 10, 6.4, 1.5, precoFinal;
    numero inteiro = números inteiros, sem casas decimais;
        exemplo: 10, -10, 0;
    numero decimal = números com casas decimais;
        exemplo: 6.4, -6.4, 0.6;
    numero real = números inteiros e casas decimais;
        exemplo: 10.5, -10.5, 0.5;
    boolean = true ou false;
        exemplo: true, false;
    null = não tem valor;
        exemplo: null;
    undefined = não foi definido;
        exemplo: undefined;
    object = objeto;
        exemplo: {}, {nome: "Lapis", preco: 1.5}, {nome: "Lapis", preco: 1.5, quantidade: 10};
    array = lista de valores;
        exemplo: [], [1, 2, 3], [1, 2, 3, 4, 5];
    function = função;
        exemplo: function(){}, function(x){return x * x}, function(x, y){return x + y};
    if = condição;
        exemplo: if(x > 0){return x * x}, if(x > 0){return x * x}else{return x * x};
    else = caso contrário;
        exemplo: if(x > 0){return x * x}, if(x > 0){return x * x}else{return x * x};
    switch = condição;
        exemplo: switch(x){case 1: return x * x; case 2: return x * x; default: return x * x};
    for = repetição;
        exemplo: for(let i = 0; i < 10; i++){console.log(i)};
    while = repetição;
        exemplo: while(x > 0){console.log(x)};
    do = repetição;
        exemplo: do{console.log(x)}; while(x > 0);
    break = interrompe a repetição;
        exemplo: for(let i = 0; i < 10; i++){console.log(i)};
    continue = pula a repetição atual;
        exemplo: for(let i = 0; i < 10; i++){console.log(i)};
    return = interrompe a função e retorna o valor;
        exemplo: function(x){return x * x};
    with = interpolação;
        exemplo: with(x){return x * x};
    try = tentativa;
        exemplo: try{return x * x}catch(e){return x * x};
    throw = lança uma exceção;
        exemplo: throw new Error("Erro");
    catch = tratamento de exceção;
        exemplo: try{return x * x}catch(e){return x * x};
    finally = finalização;
        exemplo: try{return x * x}finally{return x * x};
    debugger = interrupção de execução;
        exemplo: debugger;
    new = cria um novo objeto;
        exemplo: new Object();
    typeof = retorna o tipo de dado;
        exemplo: typeof x;
    instanceof = retorna se o objeto é uma instância de uma classe;
        exemplo: x instanceof Object;
    delete = deleta um objeto;
        exemplo: delete x;
    in = verifica se um objeto está contido em um objeto;
        exemplo: x in y;
    void = não retorna nada;
        exemplo: void x;
*/

